void	my_putchar(char c);

void	my_trspos_tab(int c, int l, int tab[l][c])
{
  int	i;
  int	j;

  for (i = 0; i < c; i = i + 1)
    {
      for (j = 0; j < l; j = j + 1)
	{
	  my_putchar(tab[j][i] + 48);
	}
      my_putchar('\n');
    }
}
